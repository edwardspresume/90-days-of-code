
x = 4

# Basic if
if x == 4:
    print('X:', x)


# If else
if x < 3:
    print('X is less then 3')
else:
    print('X is more then 3')


# else if
color = 'red'

if color == 'red':
    print('color is red')
elif color == 'blue':
    print('color is blue')
else:
    print('Neither blue or red')


# Logical operators
if x == 4 and color == 'red':
    print('x is 4 and color is red')
